    # Create launcher directly in existing PATH directory
    $launcherPath = New-LauncherScript -LauncherDir $writablePathDir -TargetExe $targetExe -CommandName "adp"
    Write-Host "  ✓ Launcher created in existing PATH directory" -ForegroundColor Green
} 
else {
    # Strategy 2: Try to add our preferred launcher directory to PATH
    Write-Host "  No writable PATH directory found, will add new directory..." -ForegroundColor Yellow
    
    $launcherDirCreated = $false
    foreach ($candidateDir in $PossibleLauncherDirs) {
        if (-not (Test-Path $candidateDir)) {
            try {
                New-Item -Path $candidateDir -ItemType Directory -Force | Out-Null
                Write-Host "  ✓ Created directory: $candidateDir" -ForegroundColor Green
            }
            catch {
                continue
            }
        }
        
        # Test if we can write to this directory
        try {
            $testFile = Join-Path $candidateDir ".adp_test_write_$(Get-Random)"
            New-Item -Path $testFile -ItemType File -Force -ErrorAction Stop | Out-Null
            Remove-Item $testFile -Force -ErrorAction SilentlyContinue
            
            # Create launcher here
            $launcherPath = New-LauncherScript -LauncherDir $candidateDir -TargetExe $targetExe -CommandName "adp"
            Write-Host "  ✓ Launcher created in: $candidateDir" -ForegroundColor Green
            
            # Add this directory to user PATH
            Add-ToUserPath -NewPath $candidateDir
            $launcherDirCreated = $true
            break
        }
        catch {
            Write-Host "  ⚠️  Cannot write to: $candidateDir" -ForegroundColor Gray
        }
    }
    
    if (-not $launcherDirCreated) {
        Write-Host "  ❌ Failed to create launcher in any directory" -ForegroundColor Red
        Write-Host "  Please manually add to PATH: $AdpBinDir" -ForegroundColor Yellow
    }
