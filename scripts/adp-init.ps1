    $newLauncherDir = $PossibleLauncherDirs | Where-Object { Test-Path $_ } | Select-Object -First 1
    if ($newLauncherDir -and ($env:PATH -notlike "*$newLauncherDir*")) {
        $env:PATH = "$newLauncherDir;$env:PATH"
        Write-Host "  ✓ Added to current session PATH: $newLauncherDir" -ForegroundColor Green
