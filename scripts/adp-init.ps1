$PossibleLauncherDirs = @(
    "$env:USERPROFILE\.local\bin",           # Modern Unix-like convention
    "$env:USERPROFILE\bin",                   # Simple user bin
    "$env:APPDATA\Microsoft\Windows\Start Menu\Programs"  # Start menu (for .bat files)
)

# Function to find a directory already in PATH
function Find-WritablePathDirectory {
    $pathDirs = $env:PATH -split ';'
    
    foreach ($dir in $pathDirs) {
        # Check if directory exists and is writable
        if (Test-Path $dir) {
            try {
                # Test write permission
                $testFile = Join-Path $dir ".adp_test_write_$(Get-Random)"
                New-Item -Path $testFile -ItemType File -Force -ErrorAction Stop | Out-Null
                Remove-Item $testFile -Force -ErrorAction SilentlyContinue
                
                Write-Host "  ✓ Found writable PATH directory: $dir" -ForegroundColor Green
                return $dir
            }
            catch {
                # Directory not writable, continue
                Write-Host "  ⚠️  Directory in PATH but not writable: $dir" -ForegroundColor Gray
            }
        }
    }
    
    return $null
}

# Function to create launcher script (batch file)
function New-LauncherScript {
    param(
        [string]$LauncherDir,
        [string]$TargetExe,
        [string]$CommandName
    )
    
    # Create batch file launcher
    $batchPath = Join-Path $LauncherDir "$CommandName.bat"
    $batchContent = @"
@echo off
REM Launcher for ADP CLI
REM Auto-generated by ADP installer

set "ADP_HOME=$AdpInstallDir"
"$TargetExe" %*
"@
    
    Set-Content -Path $batchPath -Value $batchContent -Encoding ASCII
    Write-Host "  ✓ Created batch launcher: $batchPath" -ForegroundColor Green
    
    # Also create PowerShell wrapper (optional, for better integration)
    $psPath = Join-Path $LauncherDir "$CommandName.ps1"
    $psContent = @"
# PowerShell wrapper for ADP CLI
# Auto-generated by ADP installer

`$script:ADP_HOME = "$AdpInstallDir"
& "$TargetExe" @args
"@
    
    Set-Content -Path $psPath -Value $psContent -Encoding UTF8
    Write-Host "  ✓ Created PowerShell wrapper: $psPath" -ForegroundColor Green
    
    return $batchPath
}

# Function to add directory to user PATH
function Add-ToUserPath {
    param([string]$NewPath)
    
    $currentUserPath = [Environment]::GetEnvironmentVariable("PATH", "User")
    
    # Normalize paths for comparison
    $normalizedNewPath = [System.IO.Path]::GetFullPath($NewPath.TrimEnd('\'))
    $pathItems = $currentUserPath -split ';' | Where-Object { $_ -ne '' }
    
    $exists = $false
    foreach ($item in $pathItems) {
        try {
            $normalizedItem = [System.IO.Path]::GetFullPath($item.TrimEnd('\'))
            if ($normalizedItem -eq $normalizedNewPath) {
                $exists = $true
                break
            }
        }
        catch {
            # Skip invalid paths
        }
    }
    
    if (-not $exists) {
        $updatedPath = if ($currentUserPath) { "$currentUserPath;$NewPath" } else { $NewPath }
        [Environment]::SetEnvironmentVariable("PATH", $updatedPath, "User")
        Write-Host "  ✓ Added to user PATH: $NewPath" -ForegroundColor Green
        return $true
    }
    
    Write-Host "  ✓ Already in user PATH: $NewPath" -ForegroundColor Cyan
    return $false
}

# Enhanced Python detection
function Find-Python {
    # Try PATH first
    $pythonCmd = Get-Command python, python3 -ErrorAction SilentlyContinue | Select-Object -First 1
    if ($pythonCmd) {
        return $pythonCmd
    }
    
    # Try Windows Registry
    $regPaths = @(
        "HKLM:\SOFTWARE\Python\PythonCore\*\InstallPath",
        "HKCU:\SOFTWARE\Python\PythonCore\*\InstallPath"
    )
    
    foreach ($regPath in $regPaths) {
        $pythonInstalls = Get-ItemProperty -Path $regPath -ErrorAction SilentlyContinue
        foreach ($install in $pythonInstalls) {
            $pythonExe = Join-Path $install."(default)" "python.exe"
            if (Test-Path $pythonExe) {
                return Get-Command $pythonExe
            }
        }
    }
    
    # Try common locations
    $commonPaths = @(
        "$env:LOCALAPPDATA\Programs\Python\Python*",
        "C:\Python*",
        "C:\Program Files\Python*"
    )
    
    foreach ($pattern in $commonPaths) {
        $pythonDirs = Get-ChildItem -Path $pattern -Directory -ErrorAction SilentlyContinue |
                      Where-Object { $_.Name -match 'Python3[0-9]+' } |
                      Sort-Object Name -Descending
        foreach ($dir in $pythonDirs) {
            $pythonExe = Join-Path $dir.FullName "python.exe"
            if (Test-Path $pythonExe) {
                return Get-Command $pythonExe
            }
        }
    }
    
    return $null
}

# Function to install ADP CLI using pip
function Install-ADPCLI {
    param([string]$PythonExe)
    
    Write-Host "  Installing ADP CLI using pip..." -ForegroundColor Yellow
    
    # Ensure pip is available
    $pipCmd = "$PythonExe -m pip"
    
    # Try to install/upgrade ADP CLI
    $installResult = & $pipCmd install --upgrade adp-cli 2>&1
    
    if ($LASTEXITCODE -ne 0) {
        Write-Host "  ❌ Pip installation failed" -ForegroundColor Red
        Write-Host "  Error: $installResult" -ForegroundColor Red
        return $false
    }
    
    Write-Host "  ✓ ADP CLI installed successfully" -ForegroundColor Green
    return $true
}

# Function to find installed ADP CLI executable
function Find-ADPExe {
    # First check if already installed in our unified directory
    $localExe = Join-Path $AdpBinDir "adp.exe"
    if (Test-Path $localExe) {
        return $localExe
    }
    
    # Check Python Scripts directory
    $pythonCmd = Find-Python
    if ($pythonCmd) {
        $pythonDir = Split-Path $pythonCmd.Source -Parent
        $scriptsDir = Join-Path $pythonDir "Scripts"
        $pythonExe = Join-Path $scriptsDir "adp.exe"
        if (Test-Path $pythonExe) {
            return $pythonExe
        }
    }
    
    # Check PATH
    $pathExe = Get-Command adp -ErrorAction SilentlyContinue
    if ($pathExe) {
        return $pathExe.Source
    }
    
    return $null
}

