    Write-Host "  ✓ ADP CLI already exists: $existingAdp" -ForegroundColor Green
    
    # Copy to unified directory if not already there
    if ($existingAdp -ne (Join-Path $AdpBinDir "adp.exe")) {
        Copy-Item -Path $existingAdp -Destination $AdpBinDir -Force
        Write-Host "  ✓ Copied to unified directory: $AdpBinDir" -ForegroundColor Green
    }
} else {
    Write-Host "  ⚠️  ADP CLI not found, installing..." -ForegroundColor Yellow
    
    # Find Python
    $pythonCmd = Find-Python
    if (-not $pythonCmd) {
        Write-Host "  ❌ Python not found! Cannot install ADP CLI" -ForegroundColor Red
        Write-Host "  Please install Python 3.8+ from: https://www.python.org/downloads/" -ForegroundColor Yellow
        exit 1
    }
    
    Write-Host "  ✓ Using Python: $($pythonCmd.Source)" -ForegroundColor Green
    
    # Install using pip
    if (Install-ADPCLI -PythonExe $pythonCmd.Source) {
        # Find where pip installed adp.exe
        $pythonDir = Split-Path $pythonCmd.Source -Parent
        $scriptsDir = Join-Path $pythonDir "Scripts"
        $pipInstalledExe = Join-Path $scriptsDir "adp.exe"
        
        if (Test-Path $pipInstalledExe) {
            Copy-Item -Path $pipInstalledExe -Destination $AdpBinDir -Force
            Write-Host "  ✓ Copied to unified directory: $AdpBinDir" -ForegroundColor Green
        } else {
            Write-Host "  ⚠️  Could not locate installed adp.exe" -ForegroundColor Yellow
        }
    } else {
        Write-Host "  ❌ Installation failed" -ForegroundColor Red
        exit 1
